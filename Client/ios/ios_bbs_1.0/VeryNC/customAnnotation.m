#import "customAnnotation.h"

@implementation customAnnotation
@synthesize title,coordinate;
-(id)initWithCoordinate:(CLLocationCoordinate2D) coords{
    if(self = [super init]){
        coordinate = coords;
    }
    return self;
}
-(void)dealloc{
    [title release];
    [super dealloc];
}
@end
